public class Calculator {

    public static int plus(int a, int b) {
        int c = a + b;
        return c;
    }

    public static int subtract(int a, int b) {
        int c = a - b;
        return c;
    }

    public static int multiply(int a, int b) {
        int c = a * b;
        return c;
    }

    public static int divide(int a, int b) {
        int c = a / b;
        return c;
    }
}
